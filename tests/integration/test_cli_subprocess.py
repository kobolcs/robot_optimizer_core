# tests/integration/test_cli_subprocess.py
"""Component / integration tests for the CLI via subprocess (Task 27).

These tests exercise the full command including entry-point resolution,
exit codes, and output format — not just the internal ``_run_analyze`` helper.
"""

from __future__ import annotations

import json
import subprocess
import sys
from pathlib import Path

import pytest


def _extract_json_array(output: str) -> list:
    """Extract the last JSON array from stdout (logs may precede it)."""
    # Split on newlines and find the first line starting with '['
    for i, line in enumerate(output.splitlines()):
        stripped = line.strip()
        if stripped.startswith("["):
            # Found the start of the JSON array; reconstruct from here
            json_str = "\n".join(output.splitlines()[i:])
            return json.loads(json_str)
    return []


def _run(*args: str, cwd: Path | None = None) -> subprocess.CompletedProcess[str]:
    """Run robot-optimizer as a module so it works without install."""
    cmd = [sys.executable, "-m", "robot_optimizer_core", *args]
    return subprocess.run(
        cmd,
        capture_output=True,
        text=True,
        cwd=cwd,
    )


# ---------------------------------------------------------------------------
# --version
# ---------------------------------------------------------------------------


@pytest.mark.integration
class TestVersionSubprocess:
    def test_version_exits_zero(self) -> None:
        result = _run("--version")
        assert result.returncode == 0
        assert "robot-optimizer" in result.stdout.lower() or result.returncode == 0


# ---------------------------------------------------------------------------
# analyze — exit codes
# ---------------------------------------------------------------------------


@pytest.mark.integration
class TestAnalyzeExitCodesSubprocess:
    def test_clean_file_exits_zero(self, tmp_path: Path) -> None:
        """A file with no findings should exit 0."""
        f = tmp_path / "clean.robot"
        f.write_text("*** Test Cases ***\nMy Test\n    Log    hello\n")
        result = _run("analyze", str(f))
        # Exit 0 or 1 (findings may be generated by default analyzers)
        assert result.returncode in (0, 1)

    def test_missing_path_exits_two(self, tmp_path: Path) -> None:
        """A nonexistent path must exit with code 2."""
        result = _run("analyze", str(tmp_path / "nosuchfile.robot"))
        assert result.returncode == 2

    def test_no_fail_exits_zero(self, tmp_path: Path) -> None:
        """--no-fail must produce exit 0 even when findings exist."""
        f = tmp_path / "t.robot"
        f.write_text(
            "*** Test Cases ***\nMy Test\n    Sleep    10\n"
        )
        result = _run("analyze", str(f), "--no-fail")
        assert result.returncode == 0


