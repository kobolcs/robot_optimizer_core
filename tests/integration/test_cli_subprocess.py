# ---------------------------------------------------------------------------
# analyze — --min-severity (Task 17)
# ---------------------------------------------------------------------------


@pytest.mark.integration
class TestMinSeveritySubprocess:
    def test_min_severity_error_filters_warnings(self, tmp_path: Path) -> None:
        """With --min-severity ERROR, WARNING findings must not appear."""
        f = tmp_path / "t.robot"
        f.write_text(
            "*** Test Cases ***\nMy Test\n    Sleep    2\n"
        )
        result = _run("analyze", str(f), "--format", "json", "--min-severity", "ERROR")
        findings = _extract_json_array(result.stdout)
        severities = {finding["severity"] for finding in findings}
        assert "WARNING" not in severities
        assert "INFO" not in severities

    def test_invalid_min_severity_exits_two(self, tmp_path: Path) -> None:
        f = tmp_path / "t.robot"
        f.write_text("*** Test Cases ***\nMy Test\n    Log    hi\n")
        result = _run("analyze", str(f), "--min-severity", "CRITICAL")
        assert result.returncode == 2


# ---------------------------------------------------------------------------
# list-analyzers (Task 16)
# ---------------------------------------------------------------------------


@pytest.mark.integration
class TestListAnalyzersSubprocess:
    def test_list_analyzers_text_exits_zero(self) -> None:
        result = _run("list-analyzers")
        assert result.returncode == 0
        assert "dead_code" in result.stdout

    def test_list_analyzers_json_is_valid(self) -> None:
        result = _run("list-analyzers", "--format", "json")
        assert result.returncode == 0
        # Extract the JSON array from stdout (logs may precede it)
        parsed = _extract_json_array(result.stdout)
        assert isinstance(parsed, list)
        names = [r["name"] for r in parsed]
        assert "dead_code" in names
        assert "sleep_detector" in names
        assert "naming_convention" in names

    def test_all_new_analyzers_listed(self) -> None:
        result = _run("list-analyzers", "--format", "json")
        parsed = _extract_json_array(result.stdout)
        names = {r["name"] for r in parsed}
        expected = {
            "dead_code",
            "sleep_detector",
            "hardcoded_value",
            "naming_convention",
            "setup_teardown",
            "tag_consistency",
            "test_documentation",
        }
        assert expected.issubset(names)
